import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import * as topojson from "topojson-client";
import { geoNaturalEarth1, geoPath } from "d3-geo";

const __dirname = dirname(fileURLToPath(import.meta.url));

const topoPath = join(
  __dirname,
  "..",
  "node_modules",
  "world-atlas",
  "countries-110m.json",
);
const topo = JSON.parse(readFileSync(topoPath, "utf-8"));

const countries = topojson.feature(topo, topo.objects.countries);
const land = topojson.merge(topo, topo.objects.countries.geometries);

const W = 960;
const H = 500;

const projection = geoNaturalEarth1().fitSize([W, H], land);
const pathGen = geoPath(projection);

const landPathD = pathGen(land);

const countryPaths = countries.features
  .map((f) => {
    const d = pathGen(f);
    return { id: String(f.id ?? ""), d: d ?? "" };
  })
  .filter((c) => c.d.length > 0);

const projScale = projection.scale();
const projTranslate = projection.translate();

const output = `// Auto-generated by scripts/generate-world-paths.mjs
// Projection: Natural Earth I via d3-geo (${W}×${H})
// Source: Natural Earth 110m via world-atlas npm package

export const MAP_WIDTH = ${W};
export const MAP_HEIGHT = ${H};

export const PROJ_SCALE = ${projScale};
export const PROJ_TRANSLATE: [number, number] = [${projTranslate[0]}, ${projTranslate[1]}];

export const LAND_PATH = ${JSON.stringify(landPathD)};

export const COUNTRY_PATHS: Array<{ id: string; d: string }> = ${JSON.stringify(countryPaths)};
`;

const outPath = join(__dirname, "..", "src", "data", "world-paths.ts");
writeFileSync(outPath, output, "utf-8");

console.log(
  "Projection: Natural Earth I",
  `| scale=${Math.round(projScale)}`,
  `| translate=[${Math.round(projTranslate[0])}, ${Math.round(projTranslate[1])}]`,
);
console.log(
  `Generated ${countryPaths.length} country paths (${Math.round(output.length / 1024)}KB)`,
);
